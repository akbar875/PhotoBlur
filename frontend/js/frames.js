function buildFrame(entry, index, count) {
  return {
    id: `template-${count}-${String(index + 1).padStart(2, "0")}`,
    name: `Frame ${String(index + 1).padStart(2, "0")}`,
    frameKind: `${count} Foto`,
    color: "#ffffff",
    border: "#f7d7e7",
    accent: "image",
    counts: [count],
    slotLayout: `${count}-photo`,
    src: `assets/frames/new/${count}-photo/${entry.filename}`,
    slots: entry.slots,
  };
}

